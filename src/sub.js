function sub(a,b){
    return a - b;
}

function sub(a,b,c){
    return a - b - c;
}

function sub(a,b,c,d){
    return a - b - c - d;
}

function sub(a,b,c, d, e){
    return a - b - c - d - e;
}

function sub(a,b, c, d, e, f){
    return a - b - c - d - e - f;
}

function sub(a,b, c, d, e, f, g){
    return a - b - c - d - e - f - g;
}
module.exports= sub;