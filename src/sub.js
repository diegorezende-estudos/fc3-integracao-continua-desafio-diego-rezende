function sub(a,b){
    return a - b;
}

function subThree(a,b,c){
    return a - b - c;
}

function subFour(a,b,c,d){
    return a - b - c - d;
}

function subFive(a,b,c, d, e){
    return a - b - c - d - e;
}

function subSix(a,b, c, d, e, f){
    return a - b - c - d - e - f;
}

function subSeven(a,b, c, d, e, f, g){
    return a - b - c - d - e - f - g;
}

module.exports = {
    sub,
    subThree,
    subFour,
    subFive,
    subSix,
    subSeven
};